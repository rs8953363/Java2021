package AutoboxingInboxing;

public class AutoboxingInboxing {
    public static void main(String[] args) {
        //Autoboxing
        int x = 12;
        Integer y = x;
        System.out.println(x);
        System.out.println(y);

        //Unboxing
        Integer z = 23;
        int t = z;
        System.out.println(z);
        System.out.println(t);
    }

}
